// create data for y, X, and W. y will be 64 in length.
nSmall = 64
XSmall = [];
for(var i = 0; i < nSmall; i++) {
	XSmall.push([1.0]);
}

var getCoord = function(i, h) {
	var col = Math.ceil((i + 0.0) / h);
	var row = i - Math.floor((i + 0.0) / h) * h;
	if(row == 0) {
		row = h;
	}
	return {col: col, row: row};
}

var isAround = function(coord1, coord2) {
	if((coord1.col != coord2.col || coord1.row != coord2.row) &&
		Math.abs(coord1.col - coord2.col) <= 1 && Math.abs(coord1.row - coord2.row) <= 1) {
		return true;
	} else {
		return false;
	}
}

WSmall = [];
var height = 8;
for(var i = 0; i < nSmall; i++) {
	var currentCoord = getCoord(i + 1, height);
	var row = [];
	var rowSum = 0;
	// console.log("Row " + i);
	for(var j = 0; j < nSmall; j++) {
		checkCoord = getCoord(j + 1, height);
		if(isAround(currentCoord, checkCoord)) {
			row.push(1.0);
			rowSum++;
		} else {
			row.push(0.0);
		}
	}
	// weigh row
	for(var j = 0; j < row.length; j++) {
		row[j] = (row[j] + 0.0) / rowSum;
	}
	WSmall.push(row);
}


